<script>
   

// Get all the faq elements
const faqs = document.querySelectorAll('.faq');

// Loop through each faq element
faqs.forEach(faq => {
  // Get the question and answer elements
  const question = faq.querySelector('h3');
  const answer = faq.querySelector('.answer');

  // Hide the answer and its background by default
  answer.style.display = 'none';
  answer.parentElement.style.backgroundColor = 'transparent';
  answer.parentElement.style.boxShadow = 'none'; // hide the shadow border by default

  // Add a click event listener to the question element
  question.addEventListener('click', () => {
    // Check if the answer is currently visible
    const isVisible = answer.style.display === 'block';

    // Hide all the answers and their backgrounds and shadow borders
    faqs.forEach(otherFaq => {
      const otherAnswer = otherFaq.querySelector('.answer');
      if (otherAnswer !== answer) {
        otherAnswer.style.display = 'none';
        otherAnswer.parentElement.style.backgroundColor = 'transparent';
        otherAnswer.parentElement.style.boxShadow = 'none'; // hide the shadow border
      }
    });

    // Show or hide the answer and its background and shadow border depending on its current visibility
    answer.style.display = isVisible ? 'none' : 'block';
    answer.parentElement.style.backgroundColor = isVisible ? 'transparent' : '#f0f0f0';
    answer.parentElement.style.boxShadow = isVisible ? 'none' : '0px 2px 4px rgba(0, 0, 0, 0.2)'; // show/hide the shadow border
  });
});



</script>
